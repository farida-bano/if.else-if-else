//if 
const x: number = 10;
if (x > 5) {
    console.log("x is greater than 5");
}
//if else
const x: number = 3;

if (x > 5) {
    console.log("x is greater than 5");
} else {
    console.log("x is not greater than 5");
}
//else if
const x: number = 7;
if (x > 10) {
    console.log("x is greater than 10");
} else if (x > 5) {
    console.log("x is greater than 5 but not greater than 10");
} else {
    console.log("x is less than or equal to 5");
}
